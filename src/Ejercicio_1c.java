//pares o impares

public class Ejercicio_1c {

    public static void main(String[] args) {
        System.out.println("ORDEN ASCENDENTE");
        for (int i = 5; i <= 14 ; i++) {
            System.out.println(i);
        }

        System.out.println("ORDEN DESCENDENTE");
        for (int i = 14; i >= 5 ; i--) {
            System.out.println(i);
        }
    }
}
